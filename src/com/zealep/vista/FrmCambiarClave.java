    private void btnCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarActionPerformed
       DoctorDAO doctorDAO = new DoctorDAO();
        boolean existe = false;
       String claveActual = txtClaveActual.getText();
       String claveNueva = txtClaveNueva.getText();
       existe = doctorDAO.validarClave(Integer.parseInt(FrmPrincipal.lblId.getText()), claveActual);
       if(existe)
       {
             doctorDAO.cambiarClave(Integer.parseInt(FrmPrincipal.lblId.getText()), claveNueva);
             JOptionPane.showMessageDialog(rootPane, "La contrase単a fue cambiada correctamente");
             
       }
       else
       {
           JOptionPane.showMessageDialog(rootPane, "La contrase単a actual es incorrecta");
       }
       txtClaveActual.setText("");
       txtClaveNueva.setText("");
       
       
    }//GEN-LAST:event_btnCambiarActionPerformed


