    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(dateFechaCita.getDate()==null)
        {
            JOptionPane.showMessageDialog(this, "Debes de ingresar la fecha de la cita");
        }
        else{
            Paciente p = new Paciente();
            p.setIdPaciente(Integer.parseInt(txtIdPaciente.getText()));
            cita.setPaciente(p);
            cita.setFechaCita(dateFechaCita.getDate());
           Date date = (Date)spnHora.getValue();
            DateFormat df = new SimpleDateFormat("hh:mm a");
            String hor = df.format(date);
            LocalTime localDateTime = LocalTime.parse(hor,DateTimeFormatter.ofPattern("hh:mm a"));
            cita.setHoraCita(localDateTime);
            cita.setAsunto(txtAsunto.getText());
            cita.setEstado(cbxEstado.getSelectedItem().toString());
            
            if (accion.equals("Nuevo")) {
                citaDAO.insertar(cita);
               JOptionPane.showMessageDialog(null, "La cita fue registrada correctamente");

            } else if (accion.equals("Modificar")) {
                cita.setIdCita(Integer.parseInt(txtIdCita.getText()));
                citaDAO.modificar(cita);
                JOptionPane.showMessageDialog(null, "La cita fue actualizada correctamente");

            }
            mostrar(dateFechaCita.getDate());
            limpiar();
            inhabilitar();
           
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
      int fila = tableCita.getSelectedRow();
      if(fila == -1 )
      {
        JOptionPane.showMessageDialog(null,"Debes de seleccionar una cita para modificarla","ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
      }
      
      else
      {
          accion ="Modificar";
          DefaultTableModel df = (DefaultTableModel) tableCita.getModel();
          int idCita = Integer.parseInt(df.getValueAt(fila,0).toString());
          Cita c = citaDAO.listarCitasPorId(idCita);
          txtIdCita.setText(String.valueOf(c.getIdCita()));
          txtIdPaciente.setText(String.valueOf(c.getPaciente().getIdPaciente()));
          txtApellidosPa.setText(c.getPaciente().getApellidos());
          txtNombresPa.setText(c.getPaciente().getNombres());
          txtDniPa.setText(c.getPaciente().getDni());
          txtNroHistoria.setText(c.getPaciente().getNroHistoria());
          dateFechaCita.setDate(c.getFechaCita());
          cbxEstado.setSelectedItem(c.getEstado());
  
          txtAsunto.setText(c.getAsunto());
          habilitar();
          mostrar(dateFechaCita.getDate());
      }
 
      
    }//GEN-LAST:event_btnModificarActionPerformed

