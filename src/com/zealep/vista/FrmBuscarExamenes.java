    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        txtBuscar.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(final KeyEvent e) {
                String cadena = (txtBuscar.getText());
                txtBuscar.setText(cadena);
                repaint();
                busquedaFiltro();
            }
        });
        trsFiltro = new TableRowSorter(tableExamen.getModel());
        tableExamen.setRowSorter(trsFiltro);
    }//GEN-LAST:event_txtBuscarKeyTyped

    private void tableExamenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableExamenMouseClicked
                
        
            DefaultTableModel df = (DefaultTableModel) tableExamen.getModel();
            int fila = tableExamen.convertRowIndexToModel(tableExamen.getSelectedRow());
            if(fila == -1)
            {
              JOptionPane.showMessageDialog(null, "Se debe seleccionar un registro");
            }
            
            else
            {
              
            int idExamen = Integer.parseInt(df.getValueAt(fila, 0).toString());
             boolean existe = imagenDAO.existeImagenPorExamen(idExamen);
             if(existe)
             {
            DialogGaleria.idExamen = idExamen;
            DialogGaleria dialogGaleria = new DialogGaleria(null,closable);
            dialogGaleria.setLocationRelativeTo(null);
            dialogGaleria.show();
             }
             else
             {
                JOptionPane.showMessageDialog(null,"El examen seleccionado no tiene imagenes","ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
             }
           
          
            }
    }//GEN-LAST:event_tableExamenMouseClicked


