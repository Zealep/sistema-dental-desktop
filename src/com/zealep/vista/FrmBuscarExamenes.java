    void busquedaFiltro() {

        {

            int opcionBuscar = 0;

            if (rbApellidos.isSelected()) {
                opcionBuscar = 1;
            } else if (rbNombres.isSelected()) {
                opcionBuscar = 2;
            } else if (rbDni.isSelected()) {
                opcionBuscar = 3;
            } else if (rbNroHistoria.isSelected()) {
                opcionBuscar = 4;
            } else if (rbTitulo.isSelected()) {
                opcionBuscar = 5;
            } else if (rbFecha.isSelected()) {
                opcionBuscar = 6;
            } else if (rbTipo.isSelected()) {
                opcionBuscar = 7;
            }

            trsFiltro.setRowFilter(RowFilter.regexFilter("(?i)" + txtBuscar.getText(), opcionBuscar));

        }
    }

