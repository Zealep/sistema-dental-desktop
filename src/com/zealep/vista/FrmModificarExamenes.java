    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int elemento = lstImagenes.getSelectedIndex();
        if (elemento < 0) {
            JOptionPane.showMessageDialog(null,
                    "Debe seleccionar una fila de la tabla", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        } 
        else
        {
         int confirmar = JOptionPane.showConfirmDialog(null,
                    "Esta seguro que desea Eliminar el registro? ");
         
        
        if (JOptionPane.OK_OPTION == confirmar) {
         imagenDAO.eliminarImagenPorExamenyRuta(Integer.parseInt(txtIdExamen.getText()),df.getElementAt(elemento).toString());
         df.remove(elemento);
         
        }
        
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

     private void limpiarLista()
    {
      df.removeAllElements();
    }

