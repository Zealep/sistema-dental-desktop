    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setMultiSelectionEnabled(true);
        int rpta = chooser.showOpenDialog(this);

        if(rpta==JFileChooser.APPROVE_OPTION)
        {
            for(File item:chooser.getSelectedFiles())
            {   
                Imagen i = new Imagen();
                Examen e = new Examen();
                e.setIdExamen(Integer.parseInt(txtIdExamen.getText()));
                i.setExamen(e);
                i.setRuta(item.getAbsolutePath());
                imagenDAO.insertarImagenExamen(i);
                df.addElement(item.getAbsolutePath());
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
         int fila;
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        fila = tableExamen.convertRowIndexToModel(tableExamen.getSelectedRow());

        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Se debe seleccionar un registro");
        } else {
            try {
                defaultTableModel = (DefaultTableModel) tableExamen.getModel();
                Examen examen = new Examen();
                int idExamen = Integer.parseInt(defaultTableModel.getValueAt(fila, 0).toString());
                examen = examenDAO.listarExamenPorId(idExamen);
                txtIdExamen.setText(defaultTableModel.getValueAt(fila, 0).toString());

                txtIdPaciente.setText(examen.getPaciente().getIdPaciente().toString());
                txtApellidos.setText(examen.getPaciente().getApellidos());
                txtNombres.setText(examen.getPaciente().getNombres());
                txtDni.setText(examen.getPaciente().getDni());
                txtNroHistoria.setText(examen.getPaciente().getNroHistoria());

                txtTitulo.setText(examen.getTitulo());
                dateFecha.setDate(examen.getFecha());
                txtTipo.setText(examen.getTipo());
                txtComentarios.setText(examen.getComentarios());

                List<Imagen> imagenes = new ArrayList<>();
                imagenes = imagenDAO.listarImagenesPorExamen(idExamen);
                limpiarLista();
                for(Imagen i:imagenes)
                {
                    df.addElement(i.getRuta());
                }
                habilitar();
              tabExamenes.setSelectedIndex(tabExamenes.indexOfComponent(panelModificar));

            } catch (Exception e) {
                // log.error("Ocurrio un error: "+e);
            }
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
          if (dateFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debes de ingresar la fecha del examen");
        } else {
              Examen examen = new Examen();
            Paciente p = new Paciente();
            p.setIdPaciente(Integer.parseInt(txtIdPaciente.getText()));
            examen.setPaciente(p);
            examen.setTitulo(txtTitulo.getText());
            examen.setFecha(dateFecha.getDate());
            examen.setTipo(txtTipo.getText());
            examen.setComentarios(txtComentarios.getText());
            examen.setIdExamen(Integer.parseInt(txtIdExamen.getText()));

                boolean rpta = examenDAO.modificar(examen);
                if (rpta) {
                  
                    JOptionPane.showMessageDialog(null, "El examen fue modificado correctamente");
                }
           
            limpiar();
            inhabilitar();
        }

        tabExamenes.setSelectedIndex(tabExamenes.indexOfComponent(panelBuscar));

    }//GEN-LAST:event_btnModificarActionPerformed

