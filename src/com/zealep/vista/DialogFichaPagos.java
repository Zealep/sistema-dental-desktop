    private void btnContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContratoActionPerformed
        
        DefaultTableModel df = (DefaultTableModel) tableFichaPagos.getModel();
         SimpleDateFormat sd = new SimpleDateFormat("dd 'de' MMMM 'de' yyyy", new Locale("ES"));
         List<FichaPagoDTO> fichaPagoDTOs = new ArrayList<>();
         
         Tratamiento t = tratamientoDAO.listarTratamientoPorId(IDTRATAMIENTO);
         
         String apellidos;
         String nombres;
         String nroHistoria;
         String dni;
         
         apellidos = t.getPaciente().getApellidos();
         nombres = t.getPaciente().getNombres();
         nroHistoria = t.getPaciente().getNroHistoria();
         dni = t.getPaciente().getDni();
         
         for(int i=0;i<df.getRowCount();i++)
         {
             FichaPagoDTO fichaPagoDTO = new FichaPagoDTO();
             fichaPagoDTO.setFechaPagar(df.getValueAt(i,0).toString());
             fichaPagoDTO.setFechaPago(df.getValueAt(i,1).toString());
             fichaPagoDTO.setNroCuota(df.getValueAt(i,2).toString());
             fichaPagoDTO.setEstado(df.getValueAt(i,3).toString());
             
              fichaPagoDTOs.add(fichaPagoDTO);
         }
     
        Map p = new HashMap();

        String logo = "/imagenes/diamante.png";
        p.put("logo", this.getClass().getResourceAsStream(logo));
        p.put("apellidos", apellidos);
        p.put("nombres",nombres);
        p.put("nroHistoria",nroHistoria);
        p.put("dni",dni);
        
        JasperReport report;
        JasperPrint print;

        try {

            URL url = this.getClass().getResource("/reportes/RptFichaPagos.jasper");
            report = (JasperReport) JRLoader.loadObject(url);
            //report = JasperCompileManager.compileReport(new File("").getAbsolutePath()+"\\src\\reportes\\RptContratoTratamiento.jrxml");
            print = JasperFillManager.fillReport(report, p, new JRBeanCollectionDataSource(fichaPagoDTOs));
            JasperViewer view = new JasperViewer(print, false);
            //jasperviewer como dialog
            JDialog dialog = new JDialog(this);//the owner
            dialog.setContentPane(view.getContentPane());
            dialog.setSize(view.getSize());
            dialog.setTitle("Contrato de Tratamiento");
            dialog.setVisible(true);

        } catch (JRException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnContratoActionPerformed

