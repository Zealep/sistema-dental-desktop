    public void mostrarFicha()
    {   Tratamiento tratamiento = new Tratamiento();
        tratamiento = tratamientoDAO.listarTratamientoPorId(IDTRATAMIENTO);
        List<Pago> pagos = new ArrayList<>();
        pagos = pagoDAO.listarPagosDeCuotasPorTratamiento(IDTRATAMIENTO);
        DefaultTableModel modelo = new DefaultTableModel();
        String[] titulos = {"Fecha Pagar","Fecha realizo pago","NÂ° Cuota","Estado"};
        modelo.setColumnIdentifiers(titulos);
        Object obj[] = new Object[modelo.getColumnCount()];
        
        SimpleDateFormat sd = new SimpleDateFormat("dd 'de' MMMM 'de' yyyy", new Locale("ES"));
        Date fechaInicioPago = tratamiento.getFechaInicioPago();
        int cuotas = tratamiento.getMesesTratamiento();
        
        List<String> fechasPagar = new ArrayList<>();
        List<Integer> nroCuotas = new ArrayList<>();
        List<String> fechaPagos = new ArrayList<>();
        List<String> estados = new ArrayList<>();
        
        
        //obtencion de cuotas
        for(int i=1;i<=cuotas;i++)
        {
            nroCuotas.add(i);
        }
     

        //ALGORITMO CONSTRUCCION DE PAGOS EN CUOTAS
        List<PagosCuotaDTO> fichas = new ArrayList<>();
        
        for(int i=0;i<nroCuotas.size();i++)
        {   PagosCuotaDTO f = new PagosCuotaDTO();
        
        if(i<pagos.size())
        {
        
          if(pagos.get(i).getNroCuota()==nroCuotas.get(i))
            {
               f.setFechaPago(pagos.get(i).getFechaPago());
               f.setNroCuota(nroCuotas.get(i));
               f.setEstado("CANCELADO");
            }
          
          else
          {   
               f.setFechaPago(null);
               f.setNroCuota(nroCuotas.get(i));
               f.setEstado("A DEUDA");
               
          }
             
        }
        
        else
        {
              f.setFechaPago(null);
              f.setNroCuota(nroCuotas.get(i));
              f.setEstado("A DEUDA");
        }
        
          fichas.add(f);

        }
        
      //
        
        fechasPagar.add(sd.format(fechaInicioPago));
        
        //obtencion de suma de meses
        for(int i=1;i<cuotas;i++)
        {
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(fechaInicioPago);
        calendar.add(Calendar.MONTH,i);
        Date fecha = calendar.getTime();
        String fechaPagar = sd.format(fecha);
        fechasPagar.add(fechaPagar);
        }
        
        
        //llenado de tabla fechas pagar
      for(int i=0;i <fechasPagar.size();i++)
      {
          
          obj[0]= fechasPagar.get(i);
          obj[1]= fichas.get(i).getFechaPago();
          obj[2]= fichas.get(i).getNroCuota();
          obj[3]= fichas.get(i).getEstado();
          modelo.addRow(obj);
      }
        
        tableFichaPagos.setModel(modelo);
        
    }
