    private void loginUsuario()
   {    
       DoctorDAO doctorDAO = new DoctorDAO();

        String usuario = txtUsuario.getText();
        String clave = txtClave.getText();

        Doctor doctor = doctorDAO.validarLogin(usuario, clave);
        if (doctor.getIdDoctor() != null) {

            FrmPrincipal principal = new FrmPrincipal();
            principal.show();
            FrmPrincipal.lblApellidos.setText(doctor.getApellidos());
            FrmPrincipal.lblNombre.setText(doctor.getNombres());
            FrmPrincipal.lblId.setText(String.valueOf(doctor.getIdDoctor()));

            this.dispose();
        }

        else
        {
            JOptionPane.showMessageDialog(rootPane, "Usuario o clave incorrecta, Por favor revisar !");
        }

      
    }
    
    
